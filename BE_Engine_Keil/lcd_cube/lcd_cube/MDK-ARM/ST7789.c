void LCD_Writ_Bus(uint8_t dat) 
{	
	LCD_CS_Clr();

	#if defined HW_SPI
		HAL_SPI_Transmit(&ST7789_LCD_SPI, &dat, 1, 0xffff);
	#elif defined SW_SPI
		uint8_t i;
		for(i=0;i<8;i++)
		{			  
			LCD_SCLK_Clr();
			if(dat&0x80)
			{
			LCD_MOSI_Set();
			}
			else
			{
			LCD_MOSI_Clr();
			}
			LCD_SCLK_Set();
			dat<<=1;
		}	
	#elif defined HW_SPI_DMA
	//TODO: spi+dma ??????û?????????????h??????
	//?????????Z??????h??w*h??????????'??dma????
	#endif

  	LCD_CS_Set();
}


